package com.registel.rdw.reportes;

import java.sql.Time;
import java.util.Date;

/**
 *
 * @author lider_desarrollador
 */
public class Despachador {
    
    String nombrePunto;
    boolean puntoEnVuelta;
    boolean adelantado;
    int cantidadPasajeros;
    boolean esBase;
    
    String placa;
    String numeroInterno;
    int numeroVuelta;
    String nombreRuta;
    Date fecha;
    Time horaSalida;
    Time horaLlegada;
    String nombreConductor;

    public String getNombrePunto() {
        return nombrePunto;
    }

    public void setNombrePunto(String nombrePunto) {
        this.nombrePunto = nombrePunto;
    }

    public boolean esPuntoEnVuelta() {
        return puntoEnVuelta;
    }

    public void setPuntoEnVuelta(boolean puntoEnVuelta) {
        this.puntoEnVuelta = puntoEnVuelta;
    }

    public boolean estaAdelantado() {
        return adelantado;
    }

    public void setAdelantado(boolean adelantado) {
        this.adelantado = adelantado;
    }

    public int getCantidadPasajeros() {
        return cantidadPasajeros;
    }

    public void setCantidadPasajeros(int cantidadPasajeros) {
        this.cantidadPasajeros = cantidadPasajeros;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getNumeroInterno() {
        return numeroInterno;
    }

    public void setNumeroInterno(String numeroInterno) {
        this.numeroInterno = numeroInterno;
    }

    public int getNumeroVuelta() {
        return numeroVuelta;
    }

    public void setNumeroVuelta(int numeroVuelta) {
        this.numeroVuelta = numeroVuelta;
    }

    public String getNombreRuta() {
        return nombreRuta;
    }

    public void setNombreRuta(String nombreRuta) {
        this.nombreRuta = nombreRuta;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public Time getHoraSalida() {
        return horaSalida;
    }

    public void setHoraSalida(Time horaSalida) {
        this.horaSalida = horaSalida;
    }

    public Time getHoraLlegada() {
        return horaLlegada;
    }

    public void setHoraLlegada(Time horaLlegada) {
        this.horaLlegada = horaLlegada;
    }

    public String getNombreConductor() {
        return nombreConductor;
    }

    public void setNombreConductor(String nombreConductor) {
        this.nombreConductor = nombreConductor;
    }
    
    public void setEsBase(boolean esBase) {
        this.esBase = esBase;
    }
    
    public boolean getEsBase() {
        return this.esBase;
    }

}
